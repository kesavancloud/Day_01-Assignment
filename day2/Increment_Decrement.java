public class Increment_Decrement {

	public static void main(String[] args) {
	
//		a = 5
//		++a;          // a becomes 6
//		a++;          // a becomes 7
//		--a;          // a becomes 6
//		a--;          // a becomes 5
		
		int var1 = 5, var2 = 5;

        // 5 is displayed
        // Then, var1 is increased to 6.
        System.out.println(var1++);

        // var2 is increased to 6
        // Then, var2 is displayed
        System.out.println(++var2);

	}

}
