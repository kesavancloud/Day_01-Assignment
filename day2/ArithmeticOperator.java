package week1.day2;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int num1 = 100; int num2 = 20;
		
		System.out.println("Total = " + (num1 + num2));
		System.out.println("Total = " + (num1 - num2));
		System.out.println("Total = " + (num1 * num2));
		System.out.println("Total = " + (num1 / num2));
		System.out.println("Total = " + (num1 % num2));
				
	}

}